class Neumatico {
  int n_id;
  String n_serie;
  String n_marca;
  String n_modelo;
  String n_medida;
  String n_condicion;
  String n_estado;
  String n_vehiculo;
  String n_f_registro;
  String n_disenio;
  String n_costo_soles;
  String n_costo_dolares;

  Neumatico(
      this.n_id,
      this.n_serie,
      this.n_marca,
      this.n_modelo,
      this.n_medida,
      this.n_condicion,
      this.n_estado,
      this.n_vehiculo,
      this.n_f_registro,
      this.n_disenio,
      this.n_costo_soles,
      this.n_costo_dolares) {
    this.n_id = n_id;
    this.n_serie = n_serie;
    this.n_marca = n_marca;
    this.n_modelo = n_modelo;
    this.n_medida = n_medida;
    this.n_condicion = n_condicion;
    this.n_estado = n_estado;
    this.n_vehiculo = n_vehiculo;
    this.n_f_registro = n_f_registro;
    this.n_disenio = n_disenio;
    this.n_costo_soles = n_costo_soles;
    this.n_costo_dolares = n_costo_dolares;
  }
}
